export type TranslationKey = keyof typeof TRANSLATIONS.en;

export function useTranslation() {
  const language = useSettingsStore((s) => s.language);
  return TRANSLATIONS[language];
}

export function getAILanguageInstruction(lang: Language): string {
  return TRANSLATIONS[lang].aiLang;
}

export function localizeUniversity(
  u: { name: string; nameRu?: string; nameKz?: string; description?: string; descriptionRu?: string; descriptionKz?: string },
  lang: Language,
) {
  return {
    name: (lang === "ru" ? u.nameRu : lang === "kz" ? u.nameKz : undefined) ?? u.name,
    description: (lang === "ru" ? u.descriptionRu : lang === "kz" ? u.descriptionKz : undefined) ?? u.description,
  };
}
